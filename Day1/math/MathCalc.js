"use string";

module.exports = {
    add: function (val1, val2) {
        return val1 + val2;
    },
    subs: function (val1, val2) {
        return val1 - val2;
    },
    div: function (val1, val2) {
        return val1 / val2;
    },
    mult: function (val1, val2) {
        return val1 * val2;
    }
};
